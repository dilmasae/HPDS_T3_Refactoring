package t3_refactoring;

public class T3_refactoring {

    public static void main(String[] args) {
        System.out.println("Refactoring");
        
        int number1 = 10;
        int number2 = 5;
        int sum = number1 + number2;
        
        System.out.println("Sum 1: " + sum);
        
        number1 = 1;
        number2 = 1;
        sum = number1 + number2;
        
        System.out.println("Sum 2: " + sum);
        
    }
    
}
